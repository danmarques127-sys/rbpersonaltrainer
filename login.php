(function () {
  function initCarCarousel(opts) {
    const section = document.querySelector(opts.sectionSelector);
    if (!section) return;

    const track = section.querySelector(opts.trackSelector);
    const dotsWrap = section.querySelector(opts.dotsSelector);
    const cards = Array.from(section.querySelectorAll(opts.itemSelector));

    if (!track || !dotsWrap || cards.length < 2) {
      cards.forEach(c => {
        c.style.opacity = '';
        c.style.transform = '';
        c.style.pointerEvents = '';
        c.style.position = '';
      });
      return;
    }

    let index = 0;
    let timer = null;

    const isMobile = () => window.matchMedia(opts.mobileQuery || '(max-width: 900px)').matches;

    let arrows = section.querySelector('.carousel-arrows');
    let prevBtn = null;
    let nextBtn = null;

    function ensureArrows() {
      const existing = section.querySelectorAll('.carousel-arrows');
      if (existing.length > 1) existing.forEach((el, i) => { if (i > 0) el.remove(); });

      arrows = section.querySelector('.carousel-arrows');
      if (!arrows) {
        arrows = document.createElement('div');
        arrows.className = 'carousel-arrows';
        arrows.innerHTML = `
          <button class="carousel-arrow" type="button" aria-label="Previous card">‹</button>
          <button class="carousel-arrow" type="button" aria-label="Next card">›</button>
        `;
        dotsWrap.insertAdjacentElement('afterend', arrows);
      }

      prevBtn = arrows.querySelector('.carousel-arrow:nth-child(1)');
      nextBtn = arrows.querySelector('.carousel-arrow:nth-child(2)');

      prevBtn.onclick = () => { prev(); restartAuto(); };
      nextBtn.onclick = () => { next(); restartAuto(); };
    }

    function buildDots() {
      dotsWrap.innerHTML = '';
      cards.forEach((_, i) => {
        const dot = document.createElement('button');
        dot.type = 'button';
        dot.className = 'carousel-dot';
        dot.setAttribute('aria-label', 'Go to item ' + (i + 1));
        dot.addEventListener('click', () => {
          index = i;
          render();
          restartAuto();
        });
        dotsWrap.appendChild(dot);
      });
    }

    function setActiveDot() {
      const dots = dotsWrap.querySelectorAll('.carousel-dot');
      dots.forEach((d, i) => {
        const active = i === index;
        d.classList.toggle('is-active', active);
        d.setAttribute('aria-current', active ? 'true' : 'false');
      });
    }

    function render() {
      const prevIdx = (index - 1 + cards.length) % cards.length;
      const nextIdx = (index + 1) % cards.length;

      cards.forEach((card, i) => {
        card.classList.remove('is-active', 'is-prev', 'is-next');
        if (i === index) card.classList.add('is-active');
        else if (i === prevIdx) card.classList.add('is-prev');
        else if (i === nextIdx) card.classList.add('is-next');
      });

      setActiveDot();
    }

    function next() {
      index = (index + 1) % cards.length;
      render();
    }

    function prev() {
      index = (index - 1 + cards.length) % cards.length;
      render();
    }

    function stopAuto() {
      if (timer) clearInterval(timer);
      timer = null;
    }

    function startAuto() {
      stopAuto();
      timer = setInterval(() => {
        if (!isMobile()) return;
        next();
      }, opts.autoplayMs || 4500);
    }

    function restartAuto() {
      stopAuto();
      startAuto();
    }

    let startX = 0;
    let startY = 0;
    let isDown = false;

    function onTouchStart(e) {
      if (!isMobile()) return;
      const t = e.touches && e.touches[0];
      if (!t) return;
      isDown = true;
      startX = t.clientX;
      startY = t.clientY;
      stopAuto();
    }

    function onTouchEnd(e) {
      if (!isMobile()) return;
      if (!isDown) return;
      isDown = false;

      const t = e.changedTouches && e.changedTouches[0];
      if (!t) { startAuto(); return; }

      const dx = t.clientX - startX;
      const dy = t.clientY - startY;

      if (Math.abs(dy) > Math.abs(dx)) {
        startAuto();
        return;
      }

      if (Math.abs(dx) > 40) {
        if (dx < 0) next();
        else prev();
      }

      startAuto();
    }

    function resetDesktop() {
      stopAuto();
      dotsWrap.style.display = 'none';
      if (arrows) arrows.style.display = 'none';

      cards.forEach(card => {
        card.classList.remove('is-active', 'is-prev', 'is-next');
        card.style.opacity = '1';
        card.style.pointerEvents = 'auto';
        card.style.transform = 'none';
        card.style.position = 'static';
      });

      track.style.position = 'static';
      track.style.height = 'auto';
    }

    function enableMobile() {
      cards.forEach(card => {
        card.style.opacity = '';
        card.style.pointerEvents = '';
        card.style.transform = '';
        card.style.position = '';
      });
      track.style.position = '';
      track.style.height = '';

      dotsWrap.style.display = '';
      ensureArrows();
      arrows.style.display = '';
      buildDots();

      if (index < 0 || index >= cards.length) index = 0;

      render();
      startAuto();
    }

    function sync() {
      if (isMobile()) enableMobile();
      else resetDesktop();
    }

    track.addEventListener('touchstart', onTouchStart, { passive: true });
    track.addEventListener('touchend', onTouchEnd, { passive: true });
    window.addEventListener('resize', sync);

    sync();
  }

  // How It Works (Services)
  initCarCarousel({
    sectionSelector: '#how-it-works',
    trackSelector: '.svc-steps',
    itemSelector: '.svc-steps .svc-step',
    dotsSelector: '.carousel-dots',
    mobileQuery: '(max-width: 900px)',
    autoplayMs: 4500
  });
  
  // Plans (Services)
initCarCarousel({
  sectionSelector: '#plans',
  trackSelector: '.svc-plans',
  itemSelector: '.svc-plans .svc-plan',
  dotsSelector: '.carousel-dots',
  mobileQuery: '(max-width: 900px)',
  autoplayMs: 4500
});

})();


